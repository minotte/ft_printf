#include "../ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (!s)
	{
		i = ft_putstr("(null)");
		return (i);
	}
	while (s[i] != '\0')
		i += ft_putchar(s[i]);
	return (i);
}
